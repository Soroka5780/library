DROP DATABASE IF EXISTS `library`;

CREATE DATABASE `library`;

USE `library`;

CREATE TABLE `kniznicas` (	
`ec_kniznica` INT NOT NULL AUTO_INCREMENT,
`nazov` varchar(255) NOT NULL,
`ulica` varchar(255),
`mesto` varchar(255),
`email` varchar(255),
`kontakt` varchar(255),
	
`createdAt` DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP(),
`updatedAt` DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP() ON UPDATE CURRENT_TIMESTAMP(),

PRIMARY KEY (ec_kniznica)		
);

CREATE TABLE `knihas` (	
`ec_kniha` INT NOT NULL AUTO_INCREMENT,
`ec_kniznica` INT NOT NULL,
`nazov` varchar(255) NOT NULL,
`autor` varchar(255),
`vydane` DATETIME,
`popis` varchar(255),
`vylucenie` INT(1) DEFAULT 0,
	
`createdAt` DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP(),
`updatedAt` DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP() ON UPDATE CURRENT_TIMESTAMP(),

PRIMARY KEY (ec_kniha),
FOREIGN KEY (ec_kniznica) REFERENCES kniznicas (ec_kniznica)	
	
);

CREATE TABLE `students` (	
`ec_student` INT NOT NULL AUTO_INCREMENT,
`ec_kniznica` INT NOT NULL,
`meno` varchar(255) NOT NULL,
`priezvisko` varchar(255),
`ulica` VARCHAR(255),
`mesto` varchar(255),
`email` VARCHAR(255),
`kontakt` varchar(255),
`vylucenie` INT(1) DEFAULT 0,
`createdAt` DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP(),
`updatedAt` DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP() ON UPDATE CURRENT_TIMESTAMP(),

PRIMARY KEY (ec_student),
FOREIGN KEY (ec_kniznica) REFERENCES kniznicas (ec_kniznica)	
	
);

CREATE TABLE `vypozickies` (
	`ec_vypozicka` INT NOT NULL AUTO_INCREMENT,
	`ec_kniznica` INT ,
	`ec_kniha` INT ,
	`ec_student` INT ,
	`datum_vypozicane` datetime,
	`datum_predpokladane` datetime,
	`datum_skutocne` datetime,
	
	
	
	`createdAt` DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP(),
	`updatedAt` DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP() ON UPDATE CURRENT_TIMESTAMP(),
	

	PRIMARY KEY (ec_vypozicka),
	
	FOREIGN KEY (ec_kniznica) REFERENCES kniznicas (ec_kniznica),
	FOREIGN KEY (ec_kniha) REFERENCES knihas (ec_kniha),
	FOREIGN KEY (ec_student) REFERENCES students (ec_student)
	
);


